
public class Main {

	public static void main(String[] args) {
		
		/* DefensiveProgramming */
		int num = 21;
		
		if (num<20) {
			System.out.println("given number is less than 20");	
		}
		else if (num==20) {
			System.out.println("given number is equal to 20");
		}
		else {
			System.out.println("given number is greater than 20");
		}

	}

}
