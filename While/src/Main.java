
public class Main {

	public static void main(String[] args) {
		int i = 1;
		while (i < 11) {
			System.out.println(i);
			i++;
		}
		System.out.println("End of full While loop");
		System.out.println("_________________________");
		int j = 1;
		while (j < 10) {
			System.out.println(j);
			j += 2;
		}
		System.out.println("End of odd While loop");
		System.out.println("_________________________");
		int k = 2;
		while (k < 11) {
			System.out.println(k);
			k += 2;
		}
		System.out.println("End of even While loop");
		System.out.println("_________________________");
	}

}
